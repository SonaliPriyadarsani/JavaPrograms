package com.Operator.BinaryOperator;
public class RelationalOperator 
{
	public static void main(String[] args) 
	{
		int a = 10 ;
		int b = 5 ;
		System.out.println("A Equals To B : " + (a == b));
		System.out.println("A Not Equals To B : " + (a != b));
		System.out.println("A Greater Than B : " + (a > b));
		System.out.println("A Lesser Then B: " + (a < b));
		System.out.println("A Greater Then Or Equals To B : " + (a >= b));
		System.out.println("A Lesser Then Equals To B : " + (a <= b));
	}
}
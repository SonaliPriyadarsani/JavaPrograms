package com.Operator.BinaryOperator;
public class ArithmaticOperator 
{
	public static void main(String[] args) 
	{
		System.out.println("10 + 20 = " + (10+20));
		System.out.println("10 - 20 = " + (10 - 20));
		System.out.println("10 * 20 = " + (10 * 20));
		System.out.println("20 / 10 = " + (20 / 10));
		System.out.println("10 % 2 = " + (10 % 2));
		
		System.out.println("---------------");
		
		System.out.println("Addition : " + (10+20));
		System.out.println("Substraction : " + (10-20));
		System.out.println("Multiplication : " + (10*20));
		System.out.println("Division : " + (20/10));
		System.out.println("Modules : " + (10%2));
		
		
		System.out.println("----------------");
		
		int a = 10 ;
		int b = 5 ;
		System.out.println("A : " + a);
		System.out.println("B : " + b);
		System.out.println("Addition Of A And B Is : " + (a+b));
		System.out.println("Substraction A And B Is : " + (a-b));
		System.out.println("Multiplication A And B Is : " + (a*b));
		System.out.println("Division A And B IS : " + (a/b));
		System.out.println("Modules A And B Is : " + (a%b));
	}
}
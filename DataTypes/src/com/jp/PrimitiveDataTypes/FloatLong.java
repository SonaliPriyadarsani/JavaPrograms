package com.jp.PrimitiveDataTypes;
public class FloatLong 
{
	public static void main(String[] args) 
	{
		//Float 
		float f = 345 ;//By Default Double
		System.out.println("Float : " + f);
		float float1 = 123.4f ;
		System.out.println("Float : " + float1);
		float float2 = (float)345.89;
		System.out.println( "float : " + float2);
		
		System.out.println();
		
		
		//Long
		long l = 1000000;//ByDefault Int
		System.out.println("Long : " + l);
		long long1 = 123454659683747l;
		System.out.println("Long : " + long1);
		long long2 = (long)2131276382722982390l;
		System.out.println("Long : " + long2);
	}

}
